    static MailThreadPage loadThreadPage(
            Connection connection,
            String query,
            int offset,
            int limit,
            MailThreadFilter filter) throws SQLException {
        int safeOffset = Math.max(0, offset);
        int safeLimit = Math.max(0, limit);
        String normalizedQuery = normalizeSearchQuery(query);
        boolean includeTagMatch = hasMatchingTagName(connection, normalizedQuery);
        MailThreadFilter effectiveFilter = filter == null ? MailThreadFilter.all() : filter;
        ThreadQueryFilter queryFilter = threadQueryFilter(connection, effectiveFilter);
        int totalCount = countThreads(connection, normalizedQuery, includeTagMatch, queryFilter);
        if (safeLimit == 0 || safeOffset >= totalCount) {
            return new MailThreadPage(List.of(), totalCount);
        }

        List<MailThreadSummary> threads = new ArrayList<>();
        StringBuilder sql = new StringBuilder("""
                select t.id, t.account_id, t.subject, t.participants, t.snippet, t.last_message_at, t.unread_count, t.message_count,
                    %s
                from threads t
                join accounts a on a.id = t.account_id
                """.formatted(threadAddressedToAccountSql("t", "a")));
        String whereClause = threadWhereClause("t", "a", normalizedQuery, includeTagMatch, queryFilter);
        if (!whereClause.isBlank()) {
            sql.append("where ").append(whereClause).append("\n");
        }
        sql.append("""
                order by coalesce(t.last_message_at, '') desc, t.id desc
                limit ? offset ?
                """);
        try (PreparedStatement statement = connection.prepareStatement(sql.toString())) {
            int index = bindThreadParameters(statement, 1, normalizedQuery, includeTagMatch, queryFilter);
            statement.setInt(index++, safeLimit);
            statement.setInt(index, safeOffset);
            try (ResultSet resultSet = statement.executeQuery()) {
                var threadIds = new ArrayList<Long>();
                while (resultSet.next()) {
                    long threadId = resultSet.getLong(1);
                    threadIds.add(threadId);
                    threads.add(new MailThreadSummary(
                            threadId,
                            resultSet.getString(2),
                            resultSet.getString(3),
                            resultSet.getString(4),
                            resultSet.getString(5),
                            resultSet.getString(6),
                            resultSet.getInt(7) > 0,
                            resultSet.getInt(8),
                            List.of(),
                            resultSet.getInt(9) != 0));
                }
                if (!threads.isEmpty()) {
                    Map<Long, List<String>> tagsByThreadId = loadTagsForThreads(connection, threadIds);
                    for (int i = 0; i < threads.size(); i++) {
                        MailThreadSummary thread = threads.get(i);
                        threads.set(i, new MailThreadSummary(
                                thread.threadId(),
                                thread.accountId(),
                                thread.subject(),
                                thread.participants(),
                                thread.snippet(),
                                thread.receivedAt(),
                                thread.unread(),
                                thread.messageCount(),
                                tagsByThreadId.getOrDefault(thread.threadId(), List.of()),
                                thread.addressedToAccount()));
                    }
                }
            }
        }
        return new MailThreadPage(threads, totalCount);
    }

    static MailMessageDetail loadMessage(Connection connection, long threadId) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement("""
                select id, thread_id, subject,
                    trim(coalesce(from_name, '') || case when from_email is null or from_email = '' then '' else ' <' || from_email || '>' end),
                    to_summary,
                    coalesce(sent_at, received_at),
                    body_text
                from messages
                where thread_id = ?
                order by coalesce(received_at, sent_at, '') desc, id desc
                limit 1
                """)) {
            statement.setLong(1, threadId);
            try (ResultSet resultSet = statement.executeQuery()) {
                if (resultSet.next()) {
                    long messageId = resultSet.getLong(1);
                    return new MailMessageDetail(
                            messageId,
                            resultSet.getLong(2),
                            resultSet.getString(3),
                            resultSet.getString(4),
                            resultSet.getString(5),
                            recipientSummary(connection, messageId, "CC"),
                            resultSet.getString(6),
                            resultSet.getString(7),
                            loadTagsForThread(connection, threadId));
                }
            }
        }
        try (PreparedStatement statement = connection.prepareStatement("""
                select subject, participants, snippet, last_message_at
                from threads
                where id = ?
                """)) {
            statement.setLong(1, threadId);
            try (ResultSet resultSet = statement.executeQuery()) {
                if (resultSet.next()) {
                    return new MailMessageDetail(
                            0L,
                            threadId,
                            resultSet.getString(1),
                            resultSet.getString(2),
                            "",
                            "",
                            resultSet.getString(4),
                            resultSet.getString(3),
                            loadTagsForThread(connection, threadId));
                }
            }
        }
        return new MailMessageDetail(0L, threadId, "(no message)", "", "", "", "", "", List.of());
    }

